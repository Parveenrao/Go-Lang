package main

/* A Pointer is a variable that stores that memory address of another variable  
   
   => Instead of holding value , it holds the memory location 
   => Pointer --> address of data in memory 


 * Two Operators  

   1.  & --> address of operator 
   2.  * --> deference operator
   
   x := 10
   p := &x --> p stores the address of x 

   fmt.Println(p)   ---> print address
   fmt.Println(*p) ---> // value at that address(10)


*/